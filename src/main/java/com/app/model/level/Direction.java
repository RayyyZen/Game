package com.app.model.level;

/**
 * The enumeration that contains all the directions that can be taken by an entity
 */
public enum Direction {

    /**
     * Head upwards
     */
    UP,

    /**
     * Head downwards
     */
    DOWN,

    /**
     * Head to the left
     */
    LEFT,

    /**
     * Head to the right
     */
    RIGHT,

    /**
     * No direction
     */
    NONE
}